    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        String Marca;
        Marca = txtMarca.getText();

        if (HelperInv.buscarMarca(Marca, rutaP)) {
            Inventario p = HelperInv.traerDatos2(Marca, rutaP);
            txtNombre.setText(p.getNombrep());
            txtCantidad.setText(p.getCantidad());
            jDateChooser1.setDateFormatString(p.getFechaV());
             cmbtipo.setSelectedItem(p.getTipo());
            aux = 1;

        } else {
            txtNombre.requestFocusInWindow();
            aux = 0;

        }

        JButton botonesH[] = {cmdAgregar, cmdCancelar};
        JButton botonesD[] = {cmdBuscar, cmdEliminar};
        HelperInv.habilitarBotones(botonesH);
        HelperInv.deshabilitarBotones(botonesD);

    }//GEN-LAST:event_cmdBuscarActionPerformed
    public void limpiar() {
        txtMarca.setText("");
        txtNombre.setText("");
        txtCantidad.setText("");
        jDateChooser1.setDateFormatString("");
        cmbtipo.setSelectedIndex(0);
        
        txtMarca.requestFocusInWindow();
        aux = 0;
    }
