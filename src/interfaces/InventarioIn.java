    private void cmdAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAgregarActionPerformed

        try {
            String marca, nombreP,cantidad,fechaV,Tipo;
            marca= txtMarca.getText();
            nombreP = txtNombre.getText();
            cantidad = txtCantidad.getText();
            fechaV =jDateChooser1.getDateFormatString();
            Tipo= cmbtipo.getSelectedItem().toString();
            ArrayList<Inventario> actualizarProducto;

            if (aux == 0) {

                Inventario p = new Inventario(marca, nombreP,cantidad,fechaV,Tipo);
                p.guardar(salida);
                HelperInv.llenarTabla2(tblInventario, rutaP);
                txtNombre.setText("");
                txtMarca.setText("");
                txtCantidad.setText("");
                txtMarca.requestFocusInWindow();

            } else {
                actualizarProducto = HelperInv.actualizarProducto(rutaP, marca,nombreP,cantidad,fechaV,Tipo);
                salida = new ObjectOutputStream(new FileOutputStream(rutaP));
                HelperInv.volcado2(salida, actualizarProducto);
                HelperInv.llenarTabla2(tblInventario, rutaP);
                HelperInv.mensaje(this, "Datos actulizados exitosamente", "Correcto!", 1);

                limpiar();

            }
        } catch (IOException ex) {
            System.out.println(ex.getMessage());
        }
        JButton botonesH[] = {cmdBuscar, cmdCancelar};
        JButton botonesD[] = {cmdAgregar, cmdEliminar};
        HelperInv.habilitarBotones(botonesH);
        HelperInv.deshabilitarBotones(botonesD);
    }//GEN-LAST:event_cmdAgregarActionPerformed

    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed
        int i, op;

        op = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar?", "Eliminar", JOptionPane.YES_NO_OPTION);

        if (op == JOptionPane.YES_OPTION) {

            try {
                i = tblInventario.getSelectedRow();
                inventario.remove(i);
                salida = new ObjectOutputStream(new FileOutputStream(rutaP));
                HelperInv.volcado2(salida, inventario);
                HelperInv.llenarTabla2(tblInventario, rutaP);
                txtNombre.setText("");
                txtMarca.setText("");
                txtCantidad.setText("");
                txtMarca.requestFocusInWindow();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Agregar.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Agregar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        JButton botonesH[] = {cmdBuscar, cmdCancelar};
        JButton botonesD[] = {cmdAgregar, cmdEliminar};
        HelperInv.habilitarBotones(botonesH);
        HelperInv.deshabilitarBotones(botonesD);
    }//GEN-LAST:event_cmdEliminarActionPerformed

